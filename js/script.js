function printContactsInfo(names, phones) {
    const arrNames = names.split(',');
    const arrPhones = phones.split(',');
    for (let i = 0; i < arrNames.length; i += 1) {
        console.log(`${arrNames[i].padEnd(8, ' ')}: ${arrPhones[i]}`);
    }
}

